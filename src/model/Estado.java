package model;

/**
 *
 * @author vinicius caetano
 */
public class Estado {
    
    private int codigo;
    private String estado;

    public Estado() {
    }

    public Estado(int codigo, String estado) {
        this.codigo = codigo;
        this.estado = estado;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
}
