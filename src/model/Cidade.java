package model;

/**
 *
 * @author vinicius caetano
 */
public class Cidade {
    
    private int codigo;
    private String cidade;
    private Estado estado;

    public Cidade() {
    }

    public Cidade(int codigo, String cidade, Estado estado) {
        this.codigo = codigo;
        this.cidade = cidade;
        this.estado = estado;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public Estado getEstado() {
        return estado;
    }

    public void setEstado(Estado estado) {
        this.estado = estado;
    }
    
    
    
}
